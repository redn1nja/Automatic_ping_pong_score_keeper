//			result.timing.dsp, result.timing.classification, result.timing.anomaly);
//		ei_printf(": \n");
		size_t maxval = 0;
		for (size_t ix = 0; ix < EI_CLASSIFIER_LABEL_COUNT; ix++) {
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_8<<ix, GPIO_PIN_RESET);
			if (result.classification[ix].value >= result.classification[maxval].value) {
				maxval = ix;
			}
		}
